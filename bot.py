class Bot(Client):
    def __init__(self):
        kwargs = dict(
            api_hash=API_HASH,
            api_id=APP_ID,
            plugins={"root": "plugins"},
            workers=TG_BOT_WORKERS,
            bot_token=TG_BOT_TOKEN,
        )
        if SESSION_STRING:
            kwargs["session_string"] = SESSION_STRING
            kwargs["name"] = ":memory:"
        else:
            kwargs["name"] = "Bot"
        super().__init__(**kwargs)
        self.LOGGER = LOGGER

